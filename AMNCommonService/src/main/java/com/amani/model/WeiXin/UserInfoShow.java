package com.amani.model.WeiXin;

public class UserInfoShow {
	private String strUserPhone;
	private String strUserName;
	private String strUserCardNumber;
	private String strOpenCard;
	private String strCardType;
	private String CardNumber;
	public String getStrUserPhone() {
		return strUserPhone;
	}
	public void setStrUserPhone(String strUserPhone) {
		this.strUserPhone = strUserPhone;
	}
	public String getStrUserName() {
		return strUserName;
	}
	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}
	public String getStrUserCardNumber() {
		return strUserCardNumber;
	}
	public void setStrUserCardNumber(String strUserCardNumber) {
		this.strUserCardNumber = strUserCardNumber;
	}
	public String getStrOpenCard() {
		return strOpenCard;
	}
	public void setStrOpenCard(String strOpenCard) {
		this.strOpenCard = strOpenCard;
	}
	public String getStrCardType() {
		return strCardType;
	}
	public void setStrCardType(String strCardType) {
		this.strCardType = strCardType;
	}
	public String getCardNumber() {
		return CardNumber;
	}
	public void setCardNumber(String cardNumber) {
		CardNumber = cardNumber;
	}
	
}
